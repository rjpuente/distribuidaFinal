    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (verificarCampos()){
            Item item = new Item();
            
            item.setNombreItem(jTextFieldDescripcion.getText());
            item.setPrecioItem(BigDecimal.valueOf(convertirTextoADouble(jTextFieldPrecio.getText())));
            
            try{
                itemJpaController.create(item);
                JOptionPane.showMessageDialog(null, "Producto creado correctamente");
                
                Menu menu = new Menu();
                menu.show();
                this.dispose();
            }catch (Exception ex){
                JOptionPane.showMessageDialog(null, "Verifique que el precio sea correcto");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

