    private Double convertirTextoADouble (String texto){
        try{
            return Double.valueOf(texto);
        }catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Debe ingresar solo número");
            return null;
        }
    }
    
    private Boolean verificarCampos(){
        if(jTextFieldDescripcion.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe llenar la descripción del ítem");
            return false;
        }
        
        if (jTextFieldPrecio.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe llenar el precio del ítem");
            return false;
        }
        
        return true;
    }
    
    private void inicializarTablaProductos() {
        String[] columnas = {"Descripción", "Precio"};
        DefaultTableModel model = new DefaultTableModel(columnas, 0);
        
        jTableItems.setModel(model);
        
    }
    
    private void llenarTablaItems(){
        listaItems = itemJpaController.findItemEntities();
        
        listaItems.forEach((item) -> {
            insertarItemEnTablaVisual(item);
        });
    }
    
    private void insertarItemEnTablaVisual(Item item) {
        DefaultTableModel model = (DefaultTableModel) jTableItems.getModel();
        Object[] fila = {item.getNombreItem(), item.getPrecioItem().doubleValue()};
        model.addRow(fila);
    }
