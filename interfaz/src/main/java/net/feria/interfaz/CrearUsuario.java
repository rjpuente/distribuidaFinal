    private void jButtonCrearUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearUsuarioActionPerformed
        
        Usuario u = new Usuario();
        u.setLogin(jTextFieldLogin.getText());
        u.setNombre(jTextFieldNombreUsuario.getText());
        String encrip_password = DigestUtils.sha1Hex(jTextFieldPassword.getText());
        u.setPassword(encrip_password);
        
        usuarioJpaController.create(u);
        JOptionPane.showMessageDialog(null, "Usuario creado exitosamente");
        Menu menu = new Menu();
        menu.show();
        this.dispose();
    }//GEN-LAST:event_jButtonCrearUsuarioActionPerformed

