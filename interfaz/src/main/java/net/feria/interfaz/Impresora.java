    public Impresora() {
        initComponents();
        this.setLocationRelativeTo(null);
        
        PrintService[] printServices = PrintServiceLookup.lookupPrintServices(null, null);
        String[] impresoras = new String[printServices.length];
        int i = 0;
        for (PrintService print : printServices) {
            impresoras[i] = print.getName();
            this.jComboBoxImpresoras.addItem(impresoras[i]);
            i++;
        }

        if (!file.exists()) {
            JOptionPane.showMessageDialog(null, "Debe especificar la impresora");
        } 
    }

