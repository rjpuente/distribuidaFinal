    private void jButtonGuardarImpresoraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarImpresoraActionPerformed
        FileWriter fw = null;
        try {
            //Creacion de flujo para escribir en archivo la configuracion
            fw = new FileWriter(file);
            //Buffer o flujo intermedio antes de escribir directamente en el archivo
            BufferedWriter bfw = new BufferedWriter(fw);
            bfw.write(jComboBoxImpresoras.getSelectedItem().toString());
            bfw.close();
            JOptionPane.showMessageDialog(null, "Configuraci√≥n guardada exitosamente!");
            Menu menu = new Menu();
            menu.show();
            this.dispose();
        } catch (IOException ioe) {
            ioe.printStackTrace();
        } finally {
            if (fw != null) {
                //cierre del flujo principal
                try {
                    fw.close();
                } catch (IOException ioe) {
                    ioe.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButtonGuardarImpresoraActionPerformed

